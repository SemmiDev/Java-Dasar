    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        
        Double totalBelanja = Double.parseDouble(txtTotalbelanja.getText());
        Double money = Double.parseDouble(txtJumlahBayar.getText());
        
        if (money == totalBelanja) {
            txtKembalian.setText("0");
        }else if(money < totalBelanja) {
            txtKembalian.setText("KURANG");
        }else {
            txtKembalian.setText("" + (money - totalBelanja));
        }
    }//GEN-LAST:event_btnProsesActionPerformed

    private void comboBeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBeliActionPerformed
        String discText = String.valueOf(comboBeli.getSelectedItem());
        Double totalBelanja = 0.0;
        if (discText == "Jumlah Beli") {
            totalBelanja = 0.0;
            count = "0";
            besardiscount = 1;        
        }else if (Integer.parseInt(String.valueOf(comboBeli.getSelectedItem())) == 1) {
            count = "5";
            besardiscount = 0.05;
            totalBelanja = Harga * 1 * besardiscount;
        } else if (Integer.parseInt(String.valueOf(comboBeli.getSelectedItem())) == 2) {
            count = "10";
            besardiscount = 0.1;
            totalBelanja = Harga * 2 * besardiscount;
        } else if (Integer.parseInt(String.valueOf(comboBeli.getSelectedItem())) == 3) {
            count = "15";
            besardiscount = 0.15;
            totalBelanja = Harga * 3 * besardiscount;
        } else if (Integer.parseInt(String.valueOf(comboBeli.getSelectedItem())) == 4) {
            count = "25";
            besardiscount = 0.25;
            totalBelanja = Harga * 4 * besardiscount;
        } else if (Integer.parseInt(String.valueOf(comboBeli.getSelectedItem())) == 5) {
            count = "30";
            besardiscount = 0.3;
            totalBelanja = Harga * 5 * besardiscount;
        }
        
        txtDiskon.setText(count);
        txtTotalbelanja.setText("" + totalBelanja);
    }//GEN-LAST:event_comboBeliActionPerformed

