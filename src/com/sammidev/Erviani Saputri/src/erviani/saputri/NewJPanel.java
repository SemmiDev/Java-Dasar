    private void ComboPilihanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboPilihanActionPerformed
        // TODO add your handling code here:
        
        pilihan = String.valueOf(ComboPilihan.getSelectedItem());
        
        if(pilihan.equals("Pilihan")) {
            Harga = 0;
        }else if (pilihan.equals("Vivo V19")){
            Harga = 2000000;
        }else if (pilihan.equals("Samsung A20")){
            Harga = 1500000;
        }else if (pilihan.equals("Realme V15")){
            Harga = 3000000;
        }else if (pilihan.equals("Oppo Reno 4")){
            Harga = 5000000;
        }else if (pilihan.equals("Asus")){
            Harga = 1000000;
        }else if (pilihan.equals("Iphone 15 Pro")){
            Harga = 10000000;
        }
        
        txtHarga.setText( "" + Harga);
    }//GEN-LAST:event_ComboPilihanActionPerformed

